// ------------------------------------------------------------
// int isalpha(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If belonging to alphabet ? 1 : 0
// ------------------------------------------------------------
int isalpha(int c)
{
	return(	((c >= 'A') && (c <= 'Z'))	||
				((c >= 'a') && (c <= 'z'))	);
};

// ------------------------------------------------------------
// int isdigit(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If numeric ? 1 : 0
// ------------------------------------------------------------
int isdigit(int c)
{
	return((c >= '0') && (c <= '9'));
};

// ------------------------------------------------------------
// int isalnum(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If alpha or num ? 1 : 0
// ------------------------------------------------------------
int isalnum(int c)
{
	return(	((c >= 'A') && (c <= 'Z'))	||
				((c >= 'a') && (c <= 'z'))	||
				((c >= '0') && (c <= '9'))	);
};

// ------------------------------------------------------------
// int isspace(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//	 If whitespace ? 1 : 0
// ------------------------------------------------------------
int isspace(int c)
{
	return(	(c == ' ')	||
				(c == '\f')	||
				(c == '\r')	||
				(c == '\n')	||
				(c == '\t')	||
				(c == 0x0B)	);
};

// ------------------------------------------------------------
// int isgraph(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If printable character ? 1 : 0
// ------------------------------------------------------------
int isgraph(int c)
{
	return((c >= 0x21) && (c <= 0x7E));
};

// ------------------------------------------------------------
// int isctrl(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If control character ? 1 : 0
// ------------------------------------------------------------
int isctrl(int c)
{
	return(c >= 0 && c <= 0x1f);
};

// ------------------------------------------------------------
// int islower(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If in lowercase ? 1 : 0
// ------------------------------------------------------------
int islower(int c)
{
	return((c >= 'a') && (c <= 'z'));
};

// ------------------------------------------------------------
// int isupper(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If in uppercase ? 1 : 0
// ------------------------------------------------------------
int isupper(int c)
{
	return((c >= 'A') && (c <= 'Z'));
};

// ------------------------------------------------------------
// int ispunct(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If punctuation ? 1 : 0
// ------------------------------------------------------------
int ispunct(int c)
{
	return(	((c >= 0x21) && (c <= 0x2F))	||
				((c >= 0x3A) && (c <= 0x40))	||
				((c >= 0x7A) && (c <= 0x7E))	);
};

// ------------------------------------------------------------
// int isxdigit(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  If hex digit ? 1 : 0
// ------------------------------------------------------------
int isxdigit(int c)
{
	return(	((c >= '0') && (c <= '9'))	||
				((c >= 'a') && (c <= 'f'))	||
				((c >= 'A') && (c <= 'F'))	);
};

// ------------------------------------------------------------
// int toupper(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  Uppercase character c
// ------------------------------------------------------------
int toupper(int c)
{
	if((c >= 'a') && (c <= 'z'))
		return(c - 0x20);
	return(c);
};

// ------------------------------------------------------------
// int tolower(int c)
// ------------------------------------------------------------
// Parameters
//  c		- Character to be tested
//
// Returns
//  Lowercase character c
// ------------------------------------------------------------
int tolower(int c)
{
	if((c >= 'A') && (c <= 'Z'))
		return (c + 0x20);
};
